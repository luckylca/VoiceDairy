#!/usr/bin/env node

/**
 * llama.rn 0.12.x ships its Android module source against a generated
 * NativeRNLlamaSpec. React Native 0.74's Codegen cannot parse the latest
 * TurboModule schema, while the library still retains a complete legacy
 * React Native bridge + JSI installation path.
 *
 * VoiceDairy intentionally stays on bridge mode for this proof of concept so
 * SenseVoice, PagerView and the existing 120 Hz native code are not forced
 * through a framework-wide React Native upgrade. This script restores the
 * generated Java base class and makes the JavaScript entry resolve the legacy
 * NativeModules bridge when TurboModuleRegistry does not expose RNLlama.
 */

const fs = require('fs');
const path = require('path');

const projectRoot = path.resolve(__dirname, '..');
const packageRoot = path.join(projectRoot, 'node_modules', 'llama.rn');
const packageJsonPath = path.join(packageRoot, 'package.json');
const specPath = path.join(
  packageRoot,
  'android',
  'src',
  'main',
  'java',
  'com',
  'rnllama',
  'NativeRNLlamaSpec.java',
);
const nativeModulePath = path.join(packageRoot, 'src', 'NativeRNLlama.ts');

if (!fs.existsSync(packageJsonPath)) {
  console.error('[VoiceDairy llama.rn] package is missing; run npm install again.');
  process.exit(1);
}

const installed = JSON.parse(fs.readFileSync(packageJsonPath, 'utf8'));
if (installed.version !== '0.12.6') {
  console.error(
    `[VoiceDairy llama.rn] expected llama.rn 0.12.6, found ${installed.version}. ` +
      'Review the compatibility shim before upgrading.',
  );
  process.exit(1);
}

const specSource = `package com.rnllama;

import androidx.annotation.NonNull;

import com.facebook.react.bridge.Promise;
import com.facebook.react.bridge.ReactApplicationContext;
import com.facebook.react.bridge.ReactContextBaseJavaModule;
import com.facebook.react.bridge.ReactMethod;

/**
 * Bridge-mode equivalent of the Java spec normally generated by React Native
 * Codegen. llama.rn only exposes install(), which installs its JSI bindings.
 */
public abstract class NativeRNLlamaSpec extends ReactContextBaseJavaModule {
  protected NativeRNLlamaSpec(ReactApplicationContext reactContext) {
    super(reactContext);
  }

  @Override
  @NonNull
  public abstract String getName();

  @ReactMethod
  public abstract void install(Promise promise);
}
`;

const nativeModuleSource = `import type { TurboModule } from 'react-native'
import { NativeModules, TurboModuleRegistry } from 'react-native'

export interface Spec extends TurboModule {
  install(): Promise<boolean>
}

const turboModule = TurboModuleRegistry.get<Spec>('RNLlama')
const legacyModule = NativeModules.RNLlama as Spec | undefined

const missingModule: Spec = {
  async install(): Promise<boolean> {
    const names = Object.keys(NativeModules).sort()
    const related = names.filter((name) =>
      /llama|voice|sherpa|clipboard|refresh/i.test(name),
    )
    const visible = names.slice(0, 80).join(', ') || '(none)'
    const relatedText = related.join(', ') || '(none)'

    throw new Error(
      '[RNLlama] Android native module is unavailable. ' +
        'NativeModules count=' + names.length +
        '; related modules=' + relatedText +
        '; visible modules=' + visible +
        '. The APK must register RNLlamaModule through the legacy ReactPackage path. ' +
        'Rebuild and reinstall the APK; reloading Metro cannot add native modules.',
    )
  },
}

export default turboModule ?? legacyModule ?? missingModule
`;

fs.mkdirSync(path.dirname(specPath), { recursive: true });
fs.writeFileSync(specPath, specSource, 'utf8');
fs.writeFileSync(nativeModulePath, nativeModuleSource, 'utf8');

console.log('[VoiceDairy llama.rn] bridge compatibility spec installed.');
console.log('[VoiceDairy llama.rn] legacy NativeModules fallback installed.');
